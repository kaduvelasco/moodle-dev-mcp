export function generateApiIndex(moodlePath: string): GeneratorResult {
  const output = join(moodlePath, "MOODLE_API_INDEX.md");

  try {
    const extraction = extractMoodleApi(moodlePath);
    const { counts }  = extraction;

    // Group by source file
    const byFile: Record<string, typeof extraction.functions> = {};
    for (const fn of extraction.functions) {
      if (!byFile[fn.file]) byFile[fn.file] = [];
      byFile[fn.file].push(fn);
    }

    const lines: string[] = [
      header(
        "Moodle API Index",
        "Public and deprecated functions extracted from Moodle's lib/ directory. " +
        "Private, internal, and undocumented functions are excluded."
      ),
      "## Summary",
      "",
      "| Visibility | Count |",
      "|------------|-------|",
      `| ✔ public     | ${counts.public} |`,
      `| ⚠ deprecated | ${counts.deprecated} |`,
      `| — internal   | ${counts.internal} (excluded) |`,
      `| — private    | ${counts.private} (excluded) |`,
      `| ? unverified | ${counts.unverified} (excluded) |`,
      "",
      `**Shown in this index: ${extraction.functions.length} functions**`,
      "",
      "---",
      "",
    ];

    for (const [file, fns] of Object.entries(byFile)) {
      lines.push(`## ${file}`, "");

      for (const fn of fns) {
        const summary    = fn.doc?.summary   ? ` — ${fn.doc.summary}`   : "";
        const returns    = fn.doc?.returns   ? ` → \`${fn.doc.returns}\`` : "";
        const since      = fn.doc?.since     ? ` _(since ${fn.doc.since})_` : "";
        const deprLabel  = fn.visibility === "deprecated"
          ? ` ~~**@deprecated**${fn.doc?.deprecated && fn.doc.deprecated !== "yes" ? `: ${fn.doc.deprecated}` : ""}~~`
          : "";

        lines.push(`- \`${fn.name}()\`${deprLabel}${summary}${returns}${since}`);
      }

      lines.push("");
    }

    return write(output, lines.join("\n"));
  } catch (e) {
    return { file: output, success: false, error: String(e) };
  }
}

export async function generateEventsIndex(moodlePath: string): Promise<GeneratorResult> {
  const output = join(moodlePath, "MOODLE_EVENTS_INDEX.md");

  return safely(output, async () => {
    const files = await glob("**/db/events.php", {
      cwd: moodlePath, absolute: true,
      ignore: ["vendor/**", "node_modules/**"],
    });

    const allEvents: Array<{ eventname: string; callback: string; source: string }> = [];

    for (const file of files) {
      try {
        const extraction = parseEventsPhp(file);
        if (!extraction) continue;
        const source = relative(moodlePath, file);
        for (const obs of extraction.observers) {
          allEvents.push({ eventname: obs.eventname, callback: obs.callback, source });
        }
      } catch { /* skip malformed file */ }
    }

    allEvents.sort((a, b) => a.eventname.localeCompare(b.eventname));

    const lines = [
      header("Moodle Events Index", "All event observers registered across all plugins."),
      `Sources scanned: **${files.length}** events.php files`,
      "",
      "---",
      "",
      "| Event | Callback | Source |",
      "|-------|----------|--------|",
      ...allEvents.map((e) => `| \`${e.eventname}\` | \`${e.callback}\` | ${e.source} |`),
      "",
    ];

    return write(output, lines.join("\n"));
  });
}

