export interface GeneratorResult {
  file:    string;
  success: boolean;
  error?:  string;
  skipped?: boolean;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function write(filePath: string, content: string): GeneratorResult {
  try {
    writeFileSync(filePath, content, "utf-8");
    globalCache.mark(filePath);
    return { file: filePath, success: true };
  } catch (e) {
    return { file: filePath, success: false, error: String(e) };
  }
}

function timestamp(): string {
  return new Date().toISOString().replace("T", " ").substring(0, 19);
}

function header(title: string, description: string): string {
  return [
    `# ${title}`,
    "",
    `> ${description}`,
    "",
    `_Generated by moodle-mcp on ${timestamp()}_`,
    "",
    "---",
    "",
  ].join("\n");
}

/**
 * Wraps a generator function with an error boundary.
 * Returns a failed GeneratorResult instead of throwing.
 */
async function safely(
  outputFile: string,
  fn: () => Promise<GeneratorResult>
): Promise<GeneratorResult> {
  try {
    return await fn();
  } catch (e) {
    return { file: outputFile, success: false, error: String(e) };
  }
}

/**
 * Finds plugin directories by scanning for version.php exactly 2 levels deep.
 * Uses two glob passes to avoid the invalid 'depth' option bug.
 */
async function findPluginDirs(moodlePath: string): Promise<string[]> {
  const files = await glob("*/*/version.php", {
    cwd:      moodlePath,
    absolute: true,
    ignore:   ["vendor/**", "node_modules/**", "lib/**"],
  });

  return [...new Set(
    files.map((f) => f.replace(/\/version\.php$/, ""))
  )];
}

